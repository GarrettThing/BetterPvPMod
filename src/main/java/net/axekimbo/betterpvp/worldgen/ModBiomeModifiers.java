public class ModBiomeModifiers {
    public static final ResourceKey<BiomeModifier> ADD_ENDERITE_DEBRIS = registerKey("add_enderite_debris");

    public static void bootstrap(BootstapContext<BiomeModifier> context) {
        var placedFeatures = context.lookup(Registries.PLACED_FEATURE);
        var biomes = context.lookup(Registries.BIOME);

        context.register(ADD_ENDERITE_DEBRIS, new ForgeBiomeModifiers.AddFeaturesBiomeModifier(
                biomes.getOrThrow(BiomeTags.IS_END),
                HolderSet.direct(placedFeatures.getOrThrow(ModPlacedFeatures.ENDERITE_DEBRIS_PLACED_KEY)),
                GenerationStep.Decoration.UNDERGROUND_ORES));
    }


    public static ResourceKey<BiomeModifier> registerKey(String name) {
        return ResourceKey.create(ForgeRegistries.Keys.BIOME_MODIFIERS, new ResourceLocation(BetterPvP.MODID, name));
    }
}
